public class ControlStatement1 {
    public static void main(String args[]){
        int age = 17;
        if(age==18){
            System.out.println("you can apply for license");
        } else if(age>18) {
            System.out.println("you still have time to apply for license");
        } else {
            System.out.println("wait until you turn 18");
        }
    }
}
