public class PrimitiveDataTypes {
    public static void main(String args[]){
//        char data type - stores a single character/letter or ASCII values
        char c = 'F';

//        integer data type - stores whole numbers from -2^31 to 2^31-1
        int j = 10;

//        byte data type - stores whole numbers from -128 to 127

        byte b = 4;

//        short stores whole numbers from -32,768 to 32,767
        short s = 12345;

//        long stores whole numbers from -2^63 to 2^63-1
        long l = 2222;

//float stores fractional numbers, single-precision 32-bit

        float f = 4.73333f;

//        double stores fractional numbers, double-precision 64-bit
        double d = 20.25;

        System.out.println("char: " + c);
        System.out.println("int: " + j);
        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
    }
}
